void LCD_CMD(u8 cmd)
{
  u8 i = 3;

  {
    GPIOC->ODR &= (~LCD_Pin_CorD);
    GPIOC->ODR &= (~(LCD_Pin_CS|LCD_Pin_WR));
  }
  
  GPIOE->ODR = (u32)(cmd);
  
  while(i--)
    __NOP;
//    asm("nop");
  
  {
    GPIOC->ODR |= (LCD_Pin_CS|LCD_Pin_WR);
  }

}

void LCD_DAT(const u8 dat)
{
  u8 i = 3;
  
  {
    GPIOC->ODR |= LCD_Pin_CorD;
    GPIOC->ODR &= (~(LCD_Pin_CS|LCD_Pin_WR));
  }
  
  GPIOE->ODR = (u32)(dat); 
  
  while(i--)
    __NOP;
//    asm("nop");
  
  {
    GPIOC->ODR |= (LCD_Pin_CS|LCD_Pin_WR);
  }
}
  
